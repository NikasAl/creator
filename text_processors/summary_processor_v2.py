        document_title = self.book_title or "–ü–µ—Ä–µ—Å–∫–∞–∑ –æ—Å–Ω–æ–≤–Ω—ã—Ö –∏–¥–µ–π"
        
        models_block = f"**–†–∞—Å–ø–æ–∑–Ω–∞–≤–∞–Ω–∏–µ —Ç–µ–∫—Å—Ç–∞** –∏–∑ —Å–∫–∞–Ω–æ–≤ –∫–Ω–∏–≥–∏, **–ü–µ—Ä–µ—Å–∫–∞–∑** –∏ **–æ–ø–∏—Å–∞–Ω–∏—è –∏–ª–ª—é—Å—Ç—Ä–∞—Ü–∏–π** —Å–æ–∑–¥–∞–Ω—ã –º–æ–¥–µ–ª—å—é: {self.model}\n–ò–ª–ª—é—Å—Ç—Ä–∞—Ü–∏–∏ —Å–æ–∑–¥–∞–Ω—ã –º–æ–¥–µ–ª—å—é: {self.image_model}"
        
